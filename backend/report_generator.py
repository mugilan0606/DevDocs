# ─── Colours ──────────────────────────────────────────────────────────────────
C_DARK    = HexColor("#0f172a")
C_ACCENT  = HexColor("#38bdf8")
C_MUTED   = HexColor("#64748b")
C_TEXT    = HexColor("#1e293b")
C_CODE_BG = HexColor("#f1f5f9")
C_RULE    = HexColor("#e2e8f0")


# ─── Doc template with header/footer + TOC notification ──────────────────────

class DevDocsTemplate(BaseDocTemplate):
    def __init__(self, filename, repo_name="", **kwargs):
        super().__init__(filename, **kwargs)
        self.repo_name = repo_name

    def afterFlowable(self, flowable):
        """Notify TOC when a section heading is encountered."""
        if isinstance(flowable, Paragraph):
            style = flowable.style.name
            text  = flowable.getPlainText()
            if style == "h1":
                key = f"section-{text}"
                self.canv.bookmarkPage(key)
                self.notify("TOCEntry", (0, text, self.page, key))
            elif style == "h2":
                key = f"sub-{text}"
                self.canv.bookmarkPage(key)
                self.notify("TOCEntry", (1, text, self.page, key))

    def handle_pageBegin(self):
        super().handle_pageBegin()
        c = self.canv
        w, h = A3

        # Top bar
        c.setFillColor(C_DARK)
        c.rect(0, h - 36, w, 36, fill=1, stroke=0)
        c.setFillColor(C_ACCENT)
        c.setFont("Helvetica-Bold", 11)
        c.drawString(32, h - 22, "◈  DevDocs.ai")
        c.setFillColor(HexColor("#94a3b8"))
        c.setFont("Helvetica", 9)
        c.drawRightString(w - 32, h - 22, self.repo_name)

        # Bottom bar
        c.setFillColor(C_DARK)
        c.rect(0, 0, w, 28, fill=1, stroke=0)
        c.setFillColor(HexColor("#475569"))
        c.setFont("Helvetica", 8)
        c.drawString(32, 9, "Generated by DevDocs.ai")
        c.drawRightString(w - 32, 9, f"Page {self.page}")


